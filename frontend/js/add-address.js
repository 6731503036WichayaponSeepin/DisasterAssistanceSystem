function updateAddressOnDrag(marker) {
  marker.on("dragend", function (e) {
    const latlng = e.target.getLatLng();
    fetch(`https://nominatim.openstreetmap.org/reverse?lat=${latlng.lat}&lon=${latlng.lng}&format=json`)
      .then(res => res.json())
      .then(data => {
        if (data && data.display_name) {
          document.getElementById("address").value = data.display_name;
          marker.bindPopup(`üìç ${data.display_name}`).openPopup();
        } else {
          document.getElementById("address").value = "‡πÑ‡∏°‡πà‡∏û‡∏ö‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏ó‡∏µ‡πà‡∏≠‡∏¢‡∏π‡πà";
        }
      })
      .catch(() => {
        document.getElementById("address").value = "‡πÄ‡∏Å‡∏¥‡∏î‡∏Ç‡πâ‡∏≠‡∏ú‡∏¥‡∏î‡∏û‡∏•‡∏≤‡∏î‡πÉ‡∏ô‡∏Å‡∏≤‡∏£‡∏î‡∏∂‡∏á‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏ó‡∏µ‡πà‡∏≠‡∏¢‡∏π‡πà";
      });
  });
}

